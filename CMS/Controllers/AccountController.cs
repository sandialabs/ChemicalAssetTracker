        [HttpGet]
        public async Task<IActionResult> ChangePassword()
        {
            var user = await _userManager.GetUserAsync(User);
            if (user == null)
            {
                throw new ApplicationException($"Unable to load user with ID '{_userManager.GetUserId(User)}'.");
            }
            // add information to ViewData that will cause the layout page to display
            // the correct navigation icons
            ControllerHelper.InitializeViewData(ViewData, User.Identity.Name, m_account_helper);

            var hasPassword = await _userManager.HasPasswordAsync(user);
            if (!hasPassword)
            {
                // PH: change this to redirect to the ManageController
                //return RedirectToAction(nameof(SetPassword));
            }

            var model = new Models.ManageViewModels.ChangePasswordViewModel { StatusMessage = StatusMessage };
            UserInfo userinfo = m_account_helper.GetUser(User.Identity.Name);
            // PH: I have to use HtmlString values so that they don't get urlencoded on the client side
            model.Email = new HtmlString(userinfo.Email);
            model.PhoneNumber = new HtmlString(userinfo.PhoneNumber);
            model.FirstName = new HtmlString(userinfo.FirstName);
            model.LastName = new HtmlString(userinfo.LastName);
            model.MiddleName = new HtmlString(userinfo.MiddleName);
            model.Position = new HtmlString(userinfo.Position);
            model.Workplace = new HtmlString(userinfo.Workplace);
            model.HomeLocationID = userinfo.HomeLocationID;
            return View(model);
        }

        [HttpPost]
        [ValidateAntiForgeryToken]
        public async Task<IActionResult> ChangePassword(Models.ManageViewModels.ChangePasswordViewModel model)
        {
            if (!ModelState.IsValid)
            {
                model.StatusMessage = "NOT VALID";
                return View(model);
            }

            var user = await _userManager.GetUserAsync(User);
            if (user == null)
            {
                throw new ApplicationException($"Unable to load user with ID '{_userManager.GetUserId(User)}'.");
            }

            var changePasswordResult = await _userManager.ChangePasswordAsync(user, model.OldPassword, model.NewPassword);
            if (!changePasswordResult.Succeeded)
            {
                AddErrors(changePasswordResult);
                foreach (var err in changePasswordResult.Errors)
                {
                    model.StatusMessage = err.Description;
                }
                return View(model);
            }

            await _signInManager.SignInAsync(user, isPersistent: false);
            _logger.LogInformation("User changed their password successfully.");
            StatusMessage = "Your password has been changed.";

            return RedirectToAction(nameof(ChangePassword));
        }


