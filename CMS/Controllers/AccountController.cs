        #region API
        [HttpPost]
        public AjaxResult UpdateProfile([FromBody] UserInfo profile)
        {
            AjaxResult result = new AjaxResult("AccountController.UpdateProfile");
            try
            {
                // don't allow user to change root location or roles
                UserInfo userinfo = m_account_helper.GetUser(User.Identity.Name);
                profile.UserName = userinfo.UserName;
                profile.HomeLocationID = userinfo.HomeLocationID;
                profile.HomeLocation = userinfo.HomeLocation;
                profile.Roles = null;  // make no changes
                m_account_helper.UpdateUser(profile, false);
                result.Succeed("Your profile information has been updated");
            }
            catch(Exception ex)
            {
                result.Fail(ex);
            }
            return result;
        }

        #endregion

        #region Helpers

        private void AddErrors(IdentityResult result)
        {
            foreach (var error in result.Errors)
            {
                ModelState.AddModelError(string.Empty, error.Description);
            }
        }

        private IActionResult RedirectToLocal(string returnUrl)
        {
            if (Url.IsLocalUrl(returnUrl))
            {
                return Redirect(returnUrl);
            }
            else
            {
                return RedirectToAction(nameof(HomeController.Index), "Home");
            }
        }

